        <h4>Bahan Kulit Pancake:</h4>
        <ol>
            <li>2 butir telur</li>
            <li>4 sdm tepung terigu</li>
            <li>1 sdm gula pasir</li>
            <li>1/2 sdt baking powder</li>
            <li>1/2 sdt pasta vanila</li>
            <li>4 sdm susu cair UHT</li>
        </ol><br>
        <h4>Cara Membuat:</h4>
        <ol>
            <li>Campur kuning telur, tepung terigu, susu cair, pasta vanila dan baking powder. Kemudian, aduk semua bahan sampai merata.</li>
            <li>Di wadah lain, mixer putih telur, mixer sampai mengembang kaku. Kemudian, sambil masukkan gula pasir dalam 2 tahap. </li>
            <li>Masukkan putih telur sedikit demi sedikit kedalam adonan kuning telur. Selanjutnya, aduk sampai tercampur rata.</li>
            <li>Setelah itu, siapkan baking paper dengan tinggi 5 cm, bulatkan, dan rekatkan dengan selotip.</li>
            <li>Panaskan teflon dengan api kecil, taruh kertas di tengah teflon. Setelah itu, masukkan adonan 3/4 tinggi kertas. </li>
            <li>Tutup teflon, setelah matang bagian bawah. Selanjutnya, balik dan masak sampai matang.</li>
            <li>Beri toping sesuai selera dan sajikan.</li>
        </ol>
        
