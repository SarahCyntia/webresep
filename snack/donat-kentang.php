        <h4>Cara Membuat:</h4>
        <ol>
            <li>Kocok 2 butir telur dalam mangkuk hingga berbusa, tambahkan garam, gula pasir, tepung terigu, dan mentega. Uleni hingga kalis.</li>
            <li>Tuang kentang yang sudah dihaluskan ke dalam mangkuk.</li>
            <li>Masukkan ragi ke dalam air dan aduk hingga larut, lalu masukkan air ragi tersebut dalam adonan.</li>
            <li>Secara perlahan uleni adonan hingga kalis.</li>
            <li>Tutup mangkok dengan kain. Diamkan adonan selama 1 jam agar mengembang.</li>
            <li>Setelah 1 jam, mulai memanaskan minyak goreng.</li>
            <li>Mulailah membentuk adonan menjadi bentuk donat, jangan lupa untuk memberi lubang di tengah</li>
