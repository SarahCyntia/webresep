        <h4>Bahan Bahan:</h4>
        <ol>
            - 2 buah Fiillet Ayam, iris dua melebar <br>
            - 12 batang Asparagus <br>
            - 1 buah Paprika Merah, potong tipis mamanjang <br>
            - 200 gram Keju Cheddar, potong memanjang <br>
            - 2 bungkus Sasa Tepung Bumbu Ala Kentucky (80gr) <br>
            - Minyak Goreng secukupnya <br>
        </ol>

        <h5>Bahan Saus:</h5>
        <ol>
            - 1/2 buah Bawang Bombay, potong dadu <br>
            - 3 siung Bawang Putih <br>
            - Sasa Saus Tomat <br>
            - Sasa Saus Sambal Asli <br>
            - Sasa Bumbu Ekstrak Daging Ayam <br>
            - Garam dan Gula secukupnya <br>
        </ol>

        <h4>Cara Membuat:</h4>
        <ol>
            <li>Ambil selembar ayam, letakkan 3 batang asparagus, paprika merah dan keju, gulung hingga padat dan rapih.</li>
            <li>Bungkus gulungan ayam dengan aluminium foil, lalu kukus selama 10 menit, angkat dan dinginkan.</li>
            <li>Siapkan adonan basah dan kering: <br> a. Adonan basah – campurkan 1 bungkus SASA Tepung Bumbu Ala Kentucky dengan air dingin (1:2). <br>b. Adonan kering – 1 bungkus SASA Tepung Bumbu Ala Kentucky.</li>
            <li>Gulingkan ayam ke adonan kering; Celupkan ayam kedalam adonan basah; Lalu gulingkan kembali ke dalam adonan kering, ketuk-ketuk.</li>
            <li>Goreng dalam minyak panas sedang hingga matang, angkat dan tiriskan.</li>
            <li>Saus : Panaskan minyak goreng, tumis bawang bombay dan bawang putih hingga harum. Masukkan Sasa Saus Tomat, Sasa Saus Sambal Asli, Sasa Bumbu Ekstrak Daging Ayam, garam dan gula pasir, masak hingga matang.</li>
            <li>Potong-potong roll chicken asparagus lalu sajikan dengan sausnya.</li>
        </ol>

