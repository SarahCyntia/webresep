        <h4>Cara Membuat:</h4>
        <ol>
            <li>Pastikan seluruh bahan untuk membuat risol mayo telah disiapkan. Lalu buat isi terlebih dahulu dengan mencampurkan smoked chicken, ayam rebus, mayones, saus tomat, saus sambal, susu kental manis putih, garam, air jeruk nipis, dan keju parut. Aduk hingga semuanya tercampur dengan rata. Sisihkan.</li>
            <li>Setelah itu, buat kulit dadar dengan mencampurkan tepung terigu, telur, garam, dan susu cair. Aduk hingga menjadi licin. Lalu, tambahkan mentega tawar ke dalamnya. Aduk hingga rata. Lalu, buat adonan dadar tipis-tipis. Sisihkan.</li>
            <li>Lalu, ambil selembar kulit. Kemudian, letakkan sepotong telur. Berikan isian yang telah dibuat sebelumnya dan parutan keju di atasnya. Setelah itu, lipat bagian kiri dan kanan, lalu gulung kulit.</li>
            <li>Gulingkan risol mayo di tepung terigu, lalu celupkan ke putih telur dan gulingkan kembali di tepung panir. Lakukan hingga semua bahan habis.</li>
            <li>Kemudian, siapkan wajan dan panaskan minyak. Goreng risol mayo dalam minyak panas dengan menggunakan api sedang. Goreng hingga risol mayo matang. Jika sudah, angkat dan tiriskan. Risol mayo telah siap untuk disajikan.</li>
        </ol>

