        <h4>Cara Membuat:</h4>
        <ol>
            <li>Siapkan wadah bersih, masukan tepung terigu dan tepung sagu. Aduk hingga tercampur sempurna</li>
            <li>Masukkan margarin, telur dan garam. Aduk hingg tercampur semua</li>
            <li>Adonan yang sudah jadi kemudian keluarkan dari wadah. Giling adonan menggunakan rolling pin</li>
            <li>Adonan yang sudah melebar kemudian dicetak dengan menggunakan tutup botol air mineral</li>
            <li>Adonan yang telah dicetak, bisa dilebarkan lagi atau dibiarkan begitu saja</li>
            <li>Adonan sisa kemudian dijadikan satu, gilling dan cetak lagi sehingga tidak ada adonan yang tersisa</li>
            <li>Taruh sedikit abon pada tiap adonan yang sudah dicetak</li>
            <li>Lipat abon yang sudah diisi abon, lalu bagian pinggir diplintir kecil-kecil. Lakukan hal tersebut ke semua adonan</li>
            <li>Kemudian siapkan wajan untuk menggoreng dengan api sedang, tuang minyak hingga panas</li>
            <li>Goreng adonan hingga matang atau berwarna cokelat keemasan</li>
            <li>Pastel kering isi abon siap disajikan.</li>
        </ol>

