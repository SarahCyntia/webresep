            <li>1/2 sdt jinten, sangrai</li>
            <li>1/2 sdt pala</li>
        </ol>
        <h4>Cara Membuat:</h4>
        <ol>
            <li>Potong ayam menjadi 8 bagian kemudian lumuri dengan air jeruk nipis dan 1/2 sdt garam, sisihkan.</li>
            <li>Tumis semua bumbu yang dihaluskan sampe harum, masukan lengkuas, daun salam, daun jeruk dan daun kunyit, aduk rata.</li>
            <li>Masukan potongan ayam, masak ayam hingga berubah warna.</li>
            <li>Tuang santan, kaldu bubuk rasa ayam, garam, gula pasir secukupnya, aduk hingga rata.</li>
            <li>Masak di atas api kecil hingga daging ayam empuk dan santan mengental.</li>
            <li>Angkat tuang ke wadah saji dan sajikan hangat.</li>
        </ol>

